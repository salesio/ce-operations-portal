import type { FoundationStudent } from "../types/entities";

function att(n: number): Record<string, boolean> {
  const out: Record<string, boolean> = {};
  for (let i = 1; i <= 7; i += 1) out[`class_${i}`] = i <= n;
  return out;
}

function student(
  partial: FoundationStudent & { completed: number },
): FoundationStudent {
  const completed = partial.completed;
  const percent = Math.round((completed / 7) * 100);
  const exam = Number(partial.nota_exame || partial.final_exam_score || 0);
  const full =
    partial.full_name ||
    [partial.nome, partial.apelido].filter(Boolean).join(" ").trim();
  return {
    ...partial,
    full_name: full,
    fullName: full,
    phone: partial.phone || partial.telefone || "",
    telefone: partial.telefone || partial.phone || "",
    class_attendance: partial.class_attendance || att(completed),
    completed_classes: completed,
    completed_lessons_count: completed,
    class_progress_percent: percent,
    lesson_progress_percent: percent,
    final_exam_score: exam,
    nota_exame: exam,
    final_exam_max_score: 100,
    final_exam_percentage: exam,
    souls_won_count: Number(partial.numero_de_almas_ganhas || partial.souls_won_count || 0),
    numero_de_almas_ganhas: Number(partial.numero_de_almas_ganhas || partial.souls_won_count || 0),
    soul_winning_completed: !!(partial.pratica_evangelismo || partial.soul_winning_completed),
    pratica_evangelismo: !!(partial.pratica_evangelismo || partial.soul_winning_completed),
    ready_for_exam: completed >= 7 && exam === 0,
    ready_for_graduation: !!(partial.aprovado || partial.ready_for_graduation) && exam >= 50,
    graduated: !!(partial.graduado || partial.graduated),
    graduado: !!(partial.graduado || partial.graduated),
    certificate_issued: !!(partial.certificado_emitido || partial.certificate_issued),
    certificado_emitido: !!(partial.certificado_emitido || partial.certificate_issued),
    status: partial.status || partial.estado || "Em Curso",
    estado: partial.estado || partial.status || "Em Curso",
    quarter: partial.quarter || "3º Trimestre",
    year: partial.year || 2026,
  };
}

