import type { Church } from "../types/entities";

/**
 * Seed churches matching the existing dashboard.js prototype seed.
 * Used to initialise mock / local data-layer stores when empty.
 */
function st(
  churchId: string,
  slot: number,
  day_of_week: string,
  service_name: string,
  time: string,
  service_type = "Presencial",
) {
  return {
    id: `st-${churchId}-${slot}`,
    day_of_week,
    service_name,
    time,
    service_type,
    is_active: true,
    notes: "",
  };
}

function timesFor(churchId: string, type: string) {
  if (churchId === "church-hq") {
    return [
      st(churchId, 0, "Domingo", "1º Culto", "07:45"),
      st(churchId, 1, "Domingo", "2º Culto", "09:30"),
      st(churchId, 2, "Quarta-feira", "Culto de Quarta-feira", "18:00"),
    ];
  }
  if (churchId === "church-nampula") {
    return [
      st(churchId, 0, "Domingo", "Culto de Domingo", "09:00"),
      st(churchId, 1, "Quarta-feira", "Quarta-feira do Amor", "17:00"),
    ];
  }
  if (churchId === "church-virtual" || type.includes("Online") || type.includes("Virtual")) {
    return [
      st(churchId, 0, "Domingo", "Culto de Domingo", "09:00", "Online"),
      st(churchId, 1, "Quarta-feira", "Culto de Quarta-feira", "18:00", "Online"),
    ];
  }
  return [
    st(churchId, 0, "Domingo", "Culto de Domingo", "09:00"),
    st(churchId, 1, "Quarta-feira", "Culto de Quarta-feira", "18:00"),
  ];
}

function church(
  partial: Omit<Church, "service_times" | "isActive"> & { type: string },
): Church {
  return {
    ...partial,
    church_id: partial.church_id || partial.id,
    service_times: timesFor(partial.id, partial.type),
    isActive: (partial.status || "Activa") === "Activa",
  };
}

